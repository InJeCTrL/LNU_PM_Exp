DATA SEGMENT  
	FIRST		DB '0','1','2','3','4','5','$'
	SECOND		DB '9','$'
DATA ENDS  

CODE SEGMENT 
ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA	
	MOV DS,AX			;初始化数据段基址
	LEA DI,FIRST
	LEA SI,SECOND
	
	MOV AH,09H
	MOV DX,OFFSET FIRST
	INT 21H
	MOV AH,02H
	MOV DL,'*'
	INT 21H
	MOV AH,09H
	MOV DX,OFFSET SECOND
	INT 21H
	MOV AH,02H
	MOV DL,'='
	INT 21H
	
	MOV CX,6
LOOP1:;字符转数字
	;MOV AL,BYTE PTR [DI]
	;SUB AL,30H
	MOV AL,[DI]-30H
	MOV BYTE PTR [DI],AL
	INC DI
	LOOP LOOP1
	MOV AL,BYTE PTR [SI]
	SUB AL,30H
	MOV BYTE PTR [SI],AL
	
	DEC DI
	CLC
	MOV CX,6
	XOR AH,AH
	XOR BH,BH
LOOP2:;乘法过程
	MOV AL,BYTE PTR [DI]
	MUL BYTE PTR [SI]
	AAM
	ADC AL,BH
	AAA
	MOV BH,AH
	MOV BYTE PTR [DI],AL
	DEC DI
	LOOP LOOP2
	
	INC DI
	MOV CX,6
LOOP3:;数字转字符
	MOV AL,BYTE PTR [DI]
	ADD AL,30H
	MOV BYTE PTR [DI],AL
	INC DI
	LOOP LOOP3
	
	MOV AH,09H
	MOV DX,OFFSET FIRST
	INT 21H
	MOV AH,0AH
	INT 21H
	
	MOV AX,4C00H
	INT 21H
CODE ENDS  
END START