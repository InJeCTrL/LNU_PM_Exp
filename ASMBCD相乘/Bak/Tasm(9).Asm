DATA SEGMENT  
	FIRST		DB 99H
	SECOND		DB 25H
	RESULT		DB 2 DUP(?)
DATA ENDS

CODE SEGMENT 
ASSUME CS:CODE,DS:DATA,ES:DATA
START:
	MOV AX,DATA	
	MOV DS,AX
	MOV ES,AX
	;SECOND转16进制
	MOV CL,4
	XOR AH,AH
	MOV AL,SECOND
	SHL AX,CL
	SHR AL,CL
	AAD

	MOV CL,AL
	XOR AX,AX
	CLC
LOOPADD:
	ADD AL,FIRST
	DAA
	XCHG BX,AX
	ADC AX,0
	DAA
	XCHG AX,BX
	LOOP LOOPADD
	MOV AH,BL
	
	LEA DI,RESULT
	MOV BYTE PTR [DI],AH
	INC DI
	MOV BYTE PTR [DI],AL
	
	MOV AX,4C00H
	INT 21H
CODE ENDS  
END START